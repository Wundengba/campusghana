create extension if not exists pgcrypto;

create or replace function public.set_row_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create table if not exists public.attendance (
  id bigint generated by default as identity primary key,
  student_id uuid references public.students (id) on delete cascade,
  index_number text,
  date date not null,
  status text not null default 'Present' check (status in ('Present', 'Absent', 'Late')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (student_id, date)
);

create index if not exists attendance_index_number_idx on public.attendance (index_number);
create index if not exists attendance_date_idx on public.attendance (date desc);

drop trigger if exists set_attendance_updated_at on public.attendance;
create trigger set_attendance_updated_at
before update on public.attendance
for each row execute function public.set_row_updated_at();

alter table public.attendance enable row level security;
drop policy if exists attendance_public_access on public.attendance;
create policy attendance_public_access on public.attendance
for all to anon, authenticated
using (true)
with check (true);

