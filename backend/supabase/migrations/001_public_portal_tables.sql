create table if not exists public.students (
  id uuid primary key default gen_random_uuid(),
  user_id uuid references public.users (id) on delete set null,
  index_number text unique,
  index text,
  parent_contact text,
  full_name text,
  class text,
  region text,
  aggregate numeric(5,2),
  status text default 'pending',
  enrolled boolean default true,
  gender text,
  date_of_birth date,
  personal_contact text,
  home_address text,
  home_town text,
  place_of_residence text,
  postal_town text,
  po_box text,
  photo_url text,
  is_logged_in boolean not null default false,
  last_active timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

do $$
begin
  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'students'
      and column_name = 'user_id'
  ) then
    create index if not exists students_user_id_idx on public.students (user_id);
  end if;

  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'students'
      and column_name = 'index_number'
  ) then
    create index if not exists students_index_number_idx on public.students (index_number);
  end if;

  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'students'
      and column_name = 'index'
  ) then
    create index if not exists students_index_idx on public.students ("index");
  end if;
end
$$;

